export const FormItem = ({ isShow, layout, form, editable, field, config }: any) => {
  const fieldProps: any = localFieldConfig['field-props'];

  const dispatch = useDispatch();
  const [reloading, setReloading] = useState(false);

  const visibleConditions = true;
  const editableConditions = true;
  const requiredConditions = true;

  return (
    isShow &&
    ((config?.visible || fieldProps.visible) === Visible.Conditions
      ? visibleConditions
      : (config?.visible || fieldProps.visible) === Visible.Yes) && (
      <Col {...layout}>
        <div className={styles.ChangeObjectAmount1}>
          <FormItemNumber
            disabled={
              !editable ||
              ((config?.editable || fieldProps.editable) === Editable.Conditions
                ? !editableConditions
                : (config?.editable || fieldProps.editable) === Editable.No)
            }
            form={form}
            formName={config.name || field}
            labelId="返還請求残高"
            labelTypeCode={config.label?.dictTypeCode || fieldProps.label.dictTypeCode}
            required={
              config?.required === Required.Conditions
                ? requiredConditions
                : (config.required || fieldProps.required) === Required.Yes
            }
            pattern={
              /^(-\d{1,9}(\.99|\.9[0-8]*|\.[0-8]\d*)?)|(\d{1,9}(\.99|\.9[0-8]*|\.[0-8]\d*)?)$/g
            }
            min={Number.MIN_SAFE_INTEGER}
            hiddenPrefix
            precision={0}
            // suffix={<div>aa</div>}
          />
          <div
            className={styles.icon}
            onClick={async () => {
              if (reloading) return;
              setReloading(true);
              await dispatch({
                type: 'JPCLMOfClaimAssessment/getRefundAmount',
                payload: {
                  policyNo: form.getFieldValue('policyNo'),
                },
              });
              setReloading(false);
            }}
          >
            <Icon type="sync" spin={reloading} />
          </div>
        </div>
      </Col>
    )
  );
};

const RefundAmount = ({ field, config, isShow, layout, form, editable }: any) => (
  <Authority>
    <FormItem
      field={field}
      config={config}
      isShow={isShow}
      layout={layout}
      form={form}
      editable={editable}
    />
  </Authority>
);

RefundAmount.displayName = 'RefundAmount';

