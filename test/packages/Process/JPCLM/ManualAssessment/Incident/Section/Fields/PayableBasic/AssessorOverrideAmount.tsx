export const FormItem = ({
  isShow,
  layout,
  form,
  editable,
  field,
  config,
  incidentPayableItem,
  isDeclined,
}: any) => {
  const fieldProps: any = localFieldConfig['field-props'];

  const benefitCategoryIsL = incidentPayableItem.benefitCategory === BenefitCategory.life;
  const policyCurrency = incidentPayableItem?.policyCurrency;
  const systemAmountIsEmpty = valueIsEmpty(incidentPayableItem.systemCalculationAmount);
  const visibleConditions = true;
  const editableConditions = !(!benefitCategoryIsL || isDeclined); //Rule(fieldProps['editable-condition'], form, '');
  const requiredConditions = !isDeclined && systemAmountIsEmpty && benefitCategoryIsL; //Rule(fieldProps['required-condition'], form, '');

  return (
    isShow &&
    ((config?.visible || fieldProps.visible) === Visible.Conditions
      ? visibleConditions
      : (config?.visible || fieldProps.visible) === Visible.Yes) && (
      <Col {...layout}>
        <FormItemNumber
          disabled={
            !editable ||
            ((config?.editable || fieldProps.editable) === Editable.Conditions
              ? !editableConditions
              : (config?.editable || fieldProps.editable) === Editable.No)
          }
          form={form}
          formName={config.name || field}
          labelId={config.label?.dictCode || fieldProps.label.dictCode}
          labelTypeCode={config.label?.dictTypeCode || fieldProps.label.dictTypeCode}
          required={
            config?.required === Required.Conditions
              ? requiredConditions
              : (config.required || fieldProps.required) === Required.Yes
          }
          hiddenPrefix
          precision={0}
          pattern={
            /^(-\d{1,9}(\.99|\.9[0-8]*|\.[0-8]\d*)?)|(\d{1,9}(\.99|\.9[0-8]*|\.[0-8]\d*)?)$/g
          }
          min={Number.MIN_SAFE_INTEGER}
        />
      </Col>
    )
  );
};

